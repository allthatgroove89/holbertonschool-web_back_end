class HolbertonCourse {
  constructor(name, length, students) {
    this._name = name;
    this._length = length;
    this._students = students;
  }
  // Getter for name
  get name(){
    this._name;
  }
  // Setter for name
  set name(value){
    this._name;
  }
  // Getter for length
  get length(){
    this._length;
  }
  // Setter for length
  set length(value){
    this._length = value;
  }
  // Getter for students
  get students(){
    this._students;
  }
  // Setter for students
  set students(value){
    this._students
  }
}
export default HolbertonCourse;